var website;

function gotoWebsite(website) {
  switch (website) {
    case 1:
      location.href='../../index.html';

      break;
    case 2:
      location.href='index.html';
      break;
    case 3:
      location.href='info/index.html';
      break;
    case 4:
      location.href='merchandise/index.html';
      break;
    case 5:
      location.href='contact/index.html';
      break;
    default:

  }
}
